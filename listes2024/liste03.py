# Soit 1 liste de nombres
# nb=[25,89,65,99,13,78,12,33,68,17]
# Trouver et afficher le maximum de la liste

nb = [25, 89, 65, 99, 13, 78, 12, 33, 68, 17]

# Utilisation de la fonction max pour trouver le maximum
maximum = max(nb)

# Afficher le maximum
print(f"Le maximum de la liste est : {maximum}")
